DROP TABLE CARTA;
DROP TABLE CARRO;

CREATE TABLE CARRO (ID SERIAL PRIMARY KEY, 
                    NOME VARCHAR(30) NOT NULL,
                    FABRICANTE VARCHAR(30) NOT NULL,
                    PAIS VARCHAR(30) NOT NULL,
                    POTENCIA DECIMAL NOT NULL,
                    PESO DECIMAL NOT NULL,
                    VMAX DECIMAL NOT NULL,
                    CILINDROS INTEGER NOT NULL,
                    CC INTEGER NOT NULL,
                    ANO INTEGER NOT NULL,
                    IMAGEM VARCHAR(30) NOT NULL);

CREATE TABLE CARTA (ID SERIAL PRIMARY KEY, 
                    NUMERO INTEGER NOT NULL, 
                    LETRA VARCHAR(1) NOT NULL, 
                    SUPERTRUNFO BOOLEAN NOT NULL, 
                    SELECIONADA BOOLEAN NOT NULL,
                    DESCARTADA BOOLEAN not null,
                    CARRO INTEGER NOT NULL,
                    NUM_VITORIAS INTEGER NOT NULL,
                    NUM_EMPATES INTEGER NOT NULL,
                    NUM_DERROTAS INTEGER NOT NULL,
                    CONSTRAINT FK_CARRO FOREIGN KEY(CARRO) REFERENCES CARRO(ID));

